// "main" is a special package name that tells the Go compiler this is an
// executable program.
package main

// Import the "fmt" package which provides access to functions that print to
// the console.
import "fmt"

// The "main" function is the entry point for the program. It is the first
// function that is called when the program is executed.
func main() {
	fmt.Println("Hello World!")
}
